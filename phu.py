#น้องโต๋ย
import random

def get_random_food():
    menu = [
        "ข้าวผัด", 
        "ผัดไทย", 
        "กะเพราไก่", 
        "ส้มตำ", 
        "ผัดซีอิ๊ว", 
        "ข้าวมันไก่", 
        "แกงเขียวหวาน", 
        "ราดหน้า", 
        "ขนมจีนน้ำยา", 
        "ต้มยำกุ้ง",
        "น้องโก๋ยโต๋ย"
    ]
    
    return random.choice(menu)

# เรียกใช้ฟังก์ชันเพื่อสุ่มเมนูอาหาร
today_menu = get_random_food()
print(f"วันนี้กินอะไรดี: {today_menu}")
